public void preencheTable(String consulta) {
     try {
         ClienteDAO cdao = new ClienteDAO();
         DefaultTableModel defaultTableModel = (DefaultTableModel) tblCliente.getModel();
         defaultTableModel.setNumRows(0);
         
         
         if(consulta == null) {
             ArrayList<Cliente> lista = cdao.consultarTodos();
         
         for (int i = 0; i < lista.size(); i++) {
             defaultTableModel.addRow(new Object[] {
                 lista.get(i).getId(),
                 lista.get(i).getNome(),
                 lista.get(i).getDataNascimento(),
                 lista.get(i).getEndereco(),
                 lista.get(i).getTelefone(),
                 lista.get(i).getEmail(),
                 lista.get(i).getRg(),
                 lista.get(i).getCpf(),
                 lista.get(i).getSexo()
                 
                 
             });
         }
             
         } else {
               ArrayList<Cliente> lista = cdao.consultar(consulta);
         
         for (int i = 0; i < lista.size(); i++) {
             defaultTableModel.addRow(new Object[] {
                 lista.get(i).getId(),
                 lista.get(i).getNome(),
                 lista.get(i).getEmail(),
                 lista.get(i).getCpf(),
                 lista.get(i).getTelefone()
             });
         }
             
         }
     } catch (Exception e) {
         System.err.println("Erro" + e);
     }
     
 }


